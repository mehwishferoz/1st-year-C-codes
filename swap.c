//C program to swap the values of two variables
#include<stdio.h>
void main()
{
    int x, y;
    printf("Enter the value of x: ");
    scanf("%d", &x);
    printf("Enter the value of y: ");
    scanf("%d", &y);

    x==y;
    y==x;

    printf("The value of x after swapping is %d\n", y);
    printf("The value of y after swapping is %d\n", x);
}

